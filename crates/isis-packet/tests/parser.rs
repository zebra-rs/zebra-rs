fn parse_emit(buf: &[u8]) {
    let packet = parse(buf);
    assert!(packet.is_ok());

    let (_, packet) = packet.unwrap();
    println!("{}", packet);
    let mut buf = BytesMut::new();
    packet.emit(&mut buf);

    let packet = parse(&buf);
    assert!(packet.is_ok());

    let (_, packet) = packet.unwrap();
    println!("{}", packet);
}

